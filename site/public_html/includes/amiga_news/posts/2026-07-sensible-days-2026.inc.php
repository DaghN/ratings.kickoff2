<p class="k2-news-post__prose">
	A short note on old friends.
</p>
<p class="k2-news-post__prose">
	Kick Off 2 and Sensible Soccer were never really just two football games. On the Amiga they were a personality test. One camp loved Dino Dini&rsquo;s loose ball, nervous control and &ldquo;master the physics or die trying&rdquo; school of football. The other loved Sensible&rsquo;s tiny players, smooth flow, rich career mode, and the sense that you were conducting a glorious Subbuteo riot. Magazines egged it on. Friendships were tested. Entire evenings were lost to the question of which one was &ldquo;proper&rdquo; football.
</p>
<p class="k2-news-post__prose">
	Thirty-odd years later, both communities are still playing competitively, still gathering for World Cups, still keeping original hardware alive, and the old rivalry feels less like a grudge and more like shared heritage. At heart we share the same thing: a love serious enough to keep these golden-age games alive.
</p>
